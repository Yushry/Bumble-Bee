import { Schema, model } from 'mongoose';

const orderSchema = new Schema({
  customer: {
    type: String,
    required: true
  },
  product: {
    type: String,
    required: true
  },
  quantity: {
    type: Number,
    required: true
  },
  price: {
    type: Number,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

const Order = model('Order', orderSchema);

export default Order;